package dio;

public class DioExer08 {
	public static void main(String[] args) {
		String sigla = "S";
		switch (sigla) {
			case "S":
				System.out.println("SMALL");
				break;
			case "M":
				System.out.println("MEDIUM");
				break;
			case "L":
				System.out.println("LARGE");
				break;
			default:
				System.out.println("Undefined");
		}
	}	
}
